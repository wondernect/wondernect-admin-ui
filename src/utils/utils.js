// 检测saga effect 回调函数是否有报错
export function effectTryCallback(callback, response) {
  try {
    callback(response);
  } catch (e) {
    message.error('回调函数有误，请检查');
    console.error(e);
  }
}
// saga effect之后的回调
export function callbackAfterEffect(callback, response) {
  if (response.code === 'SUCCESS' && typeof callback === 'function') {
    effectTryCallback(callback, response);
  }
}
// 从post接口下载文件
export function fileDownload({ data, response }) {
  const blobUrl = window.URL.createObjectURL(data);
  const a = document.createElement('a');
  if (response.headers.get('Content-Disposition')) {
    let filename = response.headers.get('Content-Disposition').match(/filename=(.+)/)[1];
    filename = decodeURIComponent(escape(filename));
    a.href = blobUrl;
    a.download = filename;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    window.URL.revokeObjectURL(blobUrl);
  }
}
